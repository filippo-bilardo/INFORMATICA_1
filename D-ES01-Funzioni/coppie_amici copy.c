#include <stdio.h>
#define LIMITE 10000

int somma_divisori(int n)
{
    int i, totale = 0;
    for (i = 1; i < n; i++) {
        if (n % i == 0) {
            totale += i;
        }
    }
    return totale;
}

void stampa_divisori(int num) {
  int sum=0;
  printf("%d -> ", num);
  for (int i = 1; i <= num; i++) {
    if (num % i == 0) {
      if (i==num) 
        printf("%d", i);
      else
        printf("%d+", i);
      sum+=i;
    }
  }
  printf("=%d -- ",sum);
}

int main()
{
    int numero, amico;
    for (numero = 2; numero <= LIMITE; numero++) {
        amico = somma_divisori(numero);
        if (amico != numero) {
            if (somma_divisori(amico) == numero) {
                printf("%d %d || ", numero, amico);
                stampa_divisori(numero);
                stampa_divisori(amico);
                printf("\n");
            }
        }
    }
    return 0;
}

